

public class Operators{
    public static void main(String[] args) {
        int x = 3;
        int y = 6;

        

        if (x>y){
            System.out.println("x greater then y");

        }
        else if (x<y){
            System.out.println("y greater than x");
        }
        else{
            System.out.println("x=y");

        }


        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x%y);//remainder



    }
    
}
