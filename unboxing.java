public class unboxing {
    public static void main(String[] args)
    {
         Integer  a=100;
         Double b=90.987;
         Character c='h';

     // unboxing

         int x=a;
         double y=b;
         char z=c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);




    }
    
}
