public class switchcase {
    public static void main(String[] args) {
        String weather = 'rainy';
        switch(weather){
            case 'rainy':

               System.out.println("take an umbrella");
               break;
            
            case 'sunny;':
               System.out.println("wear gunglasses");
               break;

            default:
               System.out.println("Check weather forecast");
                
        }
    }
    
}
